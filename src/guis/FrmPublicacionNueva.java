    /**
     * Guardar la informacion de la publicacion en la base de datos.
     */
    private void guardarPublicacion(){
        if ("".equals(txtContenido.getText())) {
            JOptionPane.showMessageDialog(this, "Sin contenido para publicar", "Informaci√≥n", JOptionPane.WARNING_MESSAGE);
        } else {
            List<String> tags = new ArrayList<>(Arrays.asList(txtTags.getText().split(" ")));
            postsDAO.guardar(new Post(LocalDateTime.now(), txtContenido.getText(), usuario, tags));
            this.dispose();
        }
        
    }
    
    
    
